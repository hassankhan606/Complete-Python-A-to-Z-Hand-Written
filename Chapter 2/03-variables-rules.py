# When defining variable names, certain rules must be followed: 
# variable names can contain letters, 
# digits, and underscores; they must start with a letter or an underscore; 
# they cannot contain spaces; they are case-sensitive (e.g., name and Name are different); 
# and they must not use Python's reserved keywords like if, for, or class. 
# Examples of valid variable names include harry, one8, and _seven.