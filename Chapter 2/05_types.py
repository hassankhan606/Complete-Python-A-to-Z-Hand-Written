# Types {} function is used to find the type of a given variable in python.
# It returns the type of the variable as a class object.
# Example:
a = 34
t = type(a)
print(t)

# We can also convert a variable given type to another type using the type() function.
# for this we use the type() function with the variable name as an argument.
# Example:
a = 31.2
b = int(a) # This will convert the float variable a to an integer variable b.
print(b)