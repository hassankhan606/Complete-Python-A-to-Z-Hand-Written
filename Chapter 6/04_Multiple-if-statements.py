age = int(input("Enter your age: "))

if age >= 5:
    print("You can go to school 🏫")

if age >= 10:
    print("You can ride a bicycle 🚴‍♂️")

if age >= 16:
    print("You can get a CNIC 🪪")

if age >= 18:
    print("You can vote 🗳️")

if age >= 21:
    print("You can apply for a driving license 🚗")
