# ----------------------------------------------
# LOOP IN PYTHON – DEFINITION, USE, AND EXAMPLES
# ----------------------------------------------

# Definition:
# Loop aik aisi technique hai jisse hum kisi bhi task ya statement ko bar bar repeat kar sakte hain 
# jab tak koi condition true ho. Isse code chhota aur efficient banta hai.

# Loop ka matlab hota hai kisi kaam ko bar bar repeat karna jab tak koi condition poori na ho jaye.
# Programming mein loop ka use hum is liye karte hain taake hum bar bar likhne ki bajaye code ko repeat kar saken.
# Python mein do main types ke loops hain: "for loop" aur "while loop".

# 1. For loop ka use tab hota hai jab humein pehle se pata ho kitni baar loop chalana hai.
# 2. While loop ka use tab hota hai jab tak koi condition true ho – yaani jab tak condition satisfy hoti rahe loop chalta rahe.

# Benefits of Loops:
# - Code reuse hota hai (ek hi code bar bar chal sakta hai)
# - Time saving hoti hai
# - Code short, clean aur easy to manage hota hai

# ========================
# EXAMPLES OF LOOPS IN PYTHON
# ========================

# Example 1: For loop – 1 se 5 tak numbers print karna
# for number in range(1, 6):   # yeh loop 1 se 5 tak chalega (6 exclusive hota hai)
#     print(number)           # har number ko print karega

# Output:
# 1
# 2
# 3
# 4
# 5

# Example 2: While loop – 1 se 5 tak numbers print karna
# count = 1
# while count <= 5:
#     print(count)
#     count += 1

# Output:
# 1
# 2
# 3
# 4
# 5

# Example 3: For loop se list ke items print karna
# fruits = ["apple", "banana", "mango"]
# for fruit in fruits:
#     print(fruit)

# Output:
# apple
# banana
# mango

# Yeh sab examples batate hain ke loop ka use kis tarah hota hai aur yeh programming ko kitna easy bana deta hai.
