# DEFINITION:
# A METHOD IS A FUNCTION THAT BELONGS TO AN OBJECT. IN PYTHON, METHODS ARE FUNCTIONS 
# THAT   ARE DEFINED INSIDE A CLASS AND CAN BE CALLED ON OBJECTS OF THAT CLASS.
# Methods that are use in lists are:
# append() - Adds an item to the end of the list.
# extend() - Adds all items of an iterable (list, tuple, set, etc.) to the end of the list.
# insert() - Adds an item at a specified index.
# remove() - Removes the first item with the specified value.
# pop() - Removes the item at the specified index, and returns it.
# clear() - Removes all items from the list.
# index() - Returns the index of the first item with the specified value.
# count() - Returns the number of times the specified value appears in the list.
# IN STRING WE CANNOT CHANGE THE STRING BUT IN LIST WE CAN CHANGE THE LIST.THAT'S WHY LIST IS MUTABLE.
