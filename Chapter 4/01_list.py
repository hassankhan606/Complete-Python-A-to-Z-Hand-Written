# Python lists are container to store a set of value of every data type.
# we use lists array to store multiple values in a single variable.
# Lists are mutable, meaning we can change their content without changing their identity.
# Lists are ordered, meaning the items have a defined order, and that order will not change.
# Def : A list is a collection of items that are ordered and changeable. It allows duplicate members.

# Example of list
my_home_list = ["orange","Milk",65,True,3.14]
print(my_home_list[0])

# List indexing
# List indexing starts at 0, so the first item is at index 0, the second item is at index 1, 
# and so on.
# we can also make list slicing to get a range of items from the list.
print(my_home_list[0:3])  # This will print the first three items in the list.
print(my_home_list[1:])   # This will print all items from index 1.